public class main {
    public static void main(String[] args) {
        for (int i = 2; i < 10; i++) {
            for (int j = 1; j < 10; j++) {
                System.out.printf("곱셈수 : %d\n", i);
                System.out.printf("피곱셈수 : %d\n", j);
                System.out.printf("결과 : %d\n",i*j);
                System.out.printf("\n");
            }

        }
    }
}
